const EDITOR = "Kan | Kan_Inthesky";
const CONTACT_URL = "https://discord.gg/kjzGA44RRE";
const state = { lang: "en" };

function t(k){
  let s = (I18N[state.lang] && I18N[state.lang][k]) || I18N.en[k] || k;
  s = s.replace(/\[[^\]]*Discord[^\]]*\]/g, CONTACT_URL);
  s = s.replace(/\[[^\]]*\]/g, EDITOR);
  return s;
}
function tHtml(k){
  let s = (I18N[state.lang] && I18N[state.lang][k]) || I18N.en[k] || k;
  s = s.replace(/\[[^\]]*Discord[^\]]*\]/g,
        '<a href="'+CONTACT_URL+'">'+CONTACT_URL+'</a>');
  s = s.replace(/\[[^\]]*\]/g, EDITOR);
  return s;
}

function applyLang(lang){
  if(!I18N[lang]) lang = "en";
  state.lang = lang;
  try{ localStorage.setItem("guardian_lang", lang); }catch(e){}
  document.documentElement.lang = lang;
  document.documentElement.dir = RTL.includes(lang) ? "rtl" : "ltr";
  document.querySelectorAll("[data-i18n]").forEach(el=>{ el.textContent = t(el.getAttribute("data-i18n")); });
  document.querySelectorAll("[data-i18n-html]").forEach(el=>{ el.innerHTML = tHtml(el.getAttribute("data-i18n-html")); });
  document.querySelectorAll("select.langsel").forEach(s=> s.value = lang);
  document.dispatchEvent(new CustomEvent("langchange",{ detail:{ lang } }));
}

function buildLangSelectors(){
  document.querySelectorAll("select.langsel").forEach(sel=>{
    sel.innerHTML = LANGS.map(([c,n])=>`<option value="${c}">${n}</option>`).join("");
    sel.addEventListener("change", e=> applyLang(e.target.value));
  });
}

document.addEventListener("DOMContentLoaded", ()=>{
  buildLangSelectors();
  let saved = "en";
  try{ saved = localStorage.getItem("guardian_lang") || "en"; }catch(e){}
  applyLang(saved);
});
